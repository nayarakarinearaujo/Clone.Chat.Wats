    // Definindo a classe ChatApp para representar a aplica√ß√£o de chat
    class ChatApp {
        constructor() {
            // Inicializando elementos HTML e outras propriedades relevantes
            this.input = document.getElementById('mensagemInput');
            this.button = document.getElementById('sendMessageButton');
            this.chat = document.getElementById('chat');
            this.emojiButton = document.getElementById('emojiButton');
            this.emojiPicker = document.getElementById('emojiPicker');
            this.messages = []; // Inicializando uma matriz para armazenar mensagens
            this.emojiManager = new EmojiManager(); // Instanciando o EmojiManager para gerenciar emojis
            this.setupListeners(); // Configurando ouvintes de eventos
        }

        // M√©todo para configurar ouvintes de eventos
        setupListeners() {
            // Ouvinte de evento para clicar no bot√£o de envio de mensagem
            this.button.addEventListener('click', () => this.addMessage());
            // Ouvinte de evento para pressionar a tecla Enter na entrada de mensagem
            this.input.addEventListener('keypress', (e) => {
                if (e.key === 'Enter') {
                    this.addMessage();
                }
            });
            // Ouvinte de evento para clicar no bot√£o de emoji
            this.emojiButton.addEventListener('click', () => this.openEmojiSelector());
        }

        // M√©todo para abrir o seletor de emoji
        openEmojiSelector() {
            // Limpa o seletor de emoji e o exibe
            this.emojiPicker.innerHTML = '';
            this.emojiPicker.style.display = 'block';

            // Itera sobre as categorias de emojis e adiciona bot√µes para cada categoria
            Object.keys(this.emojiManager.categoriasEmojis).forEach(categoria => {
                const categoriaButton = document.createElement('button');
                categoriaButton.textContent = categoria;
                categoriaButton.addEventListener('click', () => this.showEmojisByCategory(categoria));
                this.emojiPicker.appendChild(categoriaButton);
            });
        }

        // M√©todo para exibir emojis de uma categoria espec√≠fica
        showEmojisByCategory(categoria) {
            // Limpa o seletor de emoji
            this.emojiPicker.innerHTML = '';

            // Obt√©m a lista de emojis para a categoria especificada
            const emojis = this.emojiManager.obterEmojis(categoria);

            // Itera sobre os emojis e adiciona bot√µes para cada um
            emojis.forEach(emojiCode => {
                // Cria um elemento span para exibir o emoji
                const span = document.createElement('span');
                span.classList.add('emoji');
                // Define o c√≥digo HTML como conte√∫do do span
                span.innerHTML = emojiCode;
                // Substitui o c√≥digo HTML pelo emoji real
                const emoji = span.textContent;

                // Cria um bot√£o para o emoji
                const button = document.createElement('button');
                button.classList.add('emoji');
                // Define o texto do bot√£o como o emoji real
                button.textContent = emoji;
                // Adiciona um evento de clique para adicionar o emoji √† entrada de mensagem
                button.addEventListener('click', () => this.addEmojiToInput(emoji));
                // Adiciona o bot√£o ao seletor de emojis
                this.emojiPicker.appendChild(button);
            });
        }

        // M√©todo para adicionar um emoji √† entrada de mensagem
        addEmojiToInput(emoji) {
            const cursorPosition = this.input.selectionStart;
            // Insere o emoji na posi√ß√£o do cursor na entrada de mensagem
            this.input.value = this.input.value.slice(0, cursorPosition) + emoji + this.input.value.slice(this.input.selectionEnd);
            // Define o novo cursor ap√≥s o emoji inserido
            this.input.setSelectionRange(cursorPosition + emoji.length, cursorPosition + emoji.length);
            // Foca na entrada de mensagem e esconde o seletor de emoji
            this.input.focus();
            this.emojiPicker.style.display = '';
        }

        // M√©todo para adicionar uma mensagem √† lista de mensagens
        addMessage() {
            const mensagem = this.input.value;
            // Verifica se a mensagem n√£o est√° vazia
            if (mensagem.trim() !== '') {
                // Adiciona a mensagem √† lista de mensagens
                this.messages.push({
                    text: mensagem,
                    time: new Date().toLocaleString(),
                    sender: 'enviada',
                    confirmada: true
                });
                // Limpa a entrada de mensagem
                this.input.value = '';
                // Exibe as mensagens atualizadas e simula o recebimento de uma mensagem
                this.displayMessages();
                setTimeout(() => this.simulateReceivedMessage(), 1000);
            }
        }

        // M√©todo para simular o recebimento de uma mensagem
        simulateReceivedMessage() {
            const mensagem = "Mensagem recebida!";
            // Adiciona a mensagem recebida √† lista de mensagens
            this.messages.push({
                text: mensagem,
                time: new Date().toLocaleString(),
                sender: 'recebida',
                confirmada: true
            });
            // Exibe as mensagens atualizadas e oculta o seletor de emoji
            this.displayMessages();
            this.emojiPicker.style.display = 'none';
        }

        // M√©todo para exibir as mensagens na √°rea de chat
        displayMessages() {
            // Limpa a √°rea de chat
            this.chat.innerHTML = '';
            // Itera sobre as mensagens e as exibe na √°rea de chat
            this.messages.forEach(message => {
                const mensagemElement = document.createElement('div');
                mensagemElement.classList.add('mensagem', message.sender);

                const paragrafo = document.createElement('p');
                paragrafo.textContent = message.text;

                const horario = document.createElement('time');
                horario.classList.add('time');
                horario.textContent = message.time;

                mensagemElement.appendChild(paragrafo);
                mensagemElement.appendChild(horario);

                // Adiciona um √≠cone de confirma√ß√£o se a mensagem foi enviada
                if (message.confirmada) {
                    const iconeConfirmacao = document.createElement('i');
                    iconeConfirmacao.classList.add('confirmacao-icon-enviada');
                    iconeConfirmacao.textContent = '\u2713';
                    mensagemElement.appendChild(iconeConfirmacao);
                }

                // Adiciona a mensagem √† √°rea de chat
                this.chat.appendChild(mensagemElement);
            });

            // Mant√©m o scroll da √°rea de chat na parte inferior para exibir as mensagens mais recentes
            this.chat.scrollTop = this.chat.scrollHeight;
        }
    }

